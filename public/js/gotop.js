$(function () {
	var navBox = $("#header_top");
	navBox.hide();
	var TargetPos = 350;
	$(window).scroll(function () {
		var ScrollPos = $(window).scrollTop();
		if (ScrollPos > TargetPos) {
			navBox.fadeIn();
		}
		else {
			navBox.fadeOut();
		}
	});
});